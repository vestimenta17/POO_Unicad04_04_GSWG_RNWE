    private void actualizarTable1(){
        
        String [] encabezado = new String[6];
        encabezado[0]="Codigo";
        encabezado[1]="Placa";
        encabezado[2]="Marca";
        encabezado[3]="Precio";
        encabezado[4]="Kilometraje";
        encabezado[5]="Modelo";
        
        
        var datos = new Object[this.autoControl.listar().size()][6];
        
        var i=0;
        for(var auto:this.autoControl.listar())
        {
            datos[i][0]=auto.getCodigo();
            datos[i][1]=auto.getPlaca();
            datos[i][2]=auto.getMarca();
            datos[i][3]=auto.getPrecio();
            datos[i][4]=auto.getKilometraje();
            datos[i][5]=auto.getModelo();
            i++;
        }
        this.modeloTabla = new DefaultTableModel(datos,encabezado);
        this.jTable1.setModel(modeloTabla);
        
    }
